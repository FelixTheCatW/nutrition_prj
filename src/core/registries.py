def classify_meal_type(dish: str) -> str:
    dish_name_lower = dish.lower()
    for meal_type, keywords in MEAL_TYPE_RULES.items():
        for kw in keywords:
            if kw in dish_name_lower:
                return meal_type

    return 'обед'

